namespace MHW_Editor {
    public partial class MainWindow {
        private readonly List<dynamic> items = new List<dynamic>();
        private string targetFile;

        public MainWindow() {
            InitializeComponent();

            dg_items.AutoGeneratingColumn += Dg_items_AutoGeneratingColumn;
            dg_items.AutoGeneratedColumns += Dg_items_AutoGeneratedColumns;

            btn_open.Click += Btn_open_Click;
            btn_save.Click += Btn_save_Click;
            btn_slot_cheat.Click += Btn_slot_cheat_Click;
            btn_set_bonus_cheat.Click += Btn_set_bonus_cheat_Click;
            btn_skill_level_cheat.Click += Btn_skill_level_cheat_Click;
            btn_zenny_cheat.Click += Btn_zenny_cheat_Click;
        }

        private void Dg_items_AutoGeneratingColumn(object sender, DataGridAutoGeneratingColumnEventArgs e) {
            // ReSharper disable once SwitchStatementMissingSomeCases
            switch (e.PropertyName) {
                case nameof(IMhwItem.Bytes):
                case nameof(IMhwItem.Changed):
                    e.Cancel = true;
                    return;
            }

            if (e.PropertyName.EndsWith("Raw")) {
                e.Cancel = true;
                return;
            }

            switch (e.PropertyName) {
                case nameof(Armor.Set_Skill_1):
                case nameof(Armor.Set_Skill_2):
                case nameof(Armor.Skill_1):
                case nameof(Armor.Skill_2):
                case nameof(Armor.Skill_3): {
                    var cb = new DataGridComboBoxColumn {
                        Header = e.Column.Header,
                        ItemsSource = DataHelper.skillDataNameLookup.Keys,
                        SelectedValueBinding = new Binding(e.PropertyName)
                    };
                    e.Column = cb;
                    break;
                }
            }

            // Fix enum value display at some point.
            // https://stackoverflow.com/questions/30217660/wpf-combobox-for-enum-values-using-autogenerated-datagrid
        }

        private void Dg_items_AutoGeneratedColumns(object sender, EventArgs e) {
            dg_items.Columns.First(x => x.Header.ToString() == nameof(IMhwItem.Name)).DisplayIndex = 0;

            if (IsGem()) {
                var skill1LevelIndex = dg_items.Columns.First(x => x.Header.ToString() == nameof(Gem.Skill_1_Level)).DisplayIndex - 1;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Gem.Skill_1)).DisplayIndex = skill1LevelIndex;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Gem.Skill_2)).DisplayIndex = skill1LevelIndex + 1;
            }

            if (IsArmor()) {
                var slot3SizeIndex = dg_items.Columns.First(x => x.Header.ToString() == nameof(Armor.Set_Skill_1_Level)).DisplayIndex - 1;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Armor.Set_Skill_1)).DisplayIndex = slot3SizeIndex;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Armor.Set_Skill_2)).DisplayIndex = slot3SizeIndex + 1;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Armor.Skill_1)).DisplayIndex = slot3SizeIndex + 2;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Armor.Skill_2)).DisplayIndex = slot3SizeIndex + 3;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Armor.Skill_3)).DisplayIndex = slot3SizeIndex + 4;
            }

            if (IsMelee()) {
                var rarityIndex = dg_items.Columns.First(x => x.Header.ToString() == nameof(Melee.Rarity)).DisplayIndex;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Melee.Sharpness_Quality)).DisplayIndex = rarityIndex + 1;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Melee.Sharpness_Amount)).DisplayIndex = rarityIndex + 2;

                var defenseIndex = dg_items.Columns.First(x => x.Header.ToString() == nameof(Melee.Defense)).DisplayIndex;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Melee.Affinity)).DisplayIndex = defenseIndex + 1;
            }

            if (IsRanged()) {
                var defenseIndex = dg_items.Columns.First(x => x.Header.ToString() == nameof(Ranged.Defense)).DisplayIndex;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Ranged.Affinity)).DisplayIndex = defenseIndex + 1;

                var slotSize3Index = dg_items.Columns.First(x => x.Header.ToString() == nameof(Ranged.Slot_3_Size)).DisplayIndex;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Ranged.Special_Ammo_Type)).DisplayIndex = slotSize3Index + 1;
                dg_items.Columns.First(x => x.Header.ToString() == nameof(Ranged.Skill)).DisplayIndex = slotSize3Index + 1;
            }

            foreach (var column in dg_items.Columns) {
                switch (column.Header.ToString()) {
                    case nameof(Armor.Set_Id):
                        column.Header = "Set (Layered) Id";
                        break;
                }

                if (column.Header.ToString().Contains("Hidden_Element")) {
                    column.Header = column.Header.ToString().Replace("Hidden_Element", "Element (Hidden)");
                }

                column.Header = ((string) column.Header).Replace("_", " ");
            }
        }

        private void Btn_open_Click(object sender, RoutedEventArgs e) {
            Open();
            if (string.IsNullOrEmpty(targetFile)) return;
            Load();

            dg_items.ItemsSource = null;
            dg_items.ItemsSource = items;
        }

        private void Btn_save_Click(object sender, RoutedEventArgs e) {
            if (string.IsNullOrEmpty(targetFile)) return;
            Save();
        }

        private void Btn_slot_cheat_Click(object sender, RoutedEventArgs e) {
            if (string.IsNullOrEmpty(targetFile)) return;

            if (!IsMelee() && !IsRanged() && !IsArmor()) return;

            foreach (var item in items) {
                ISlots itemWithSlots = item;
                itemWithSlots.Slot_Count = 3;
                itemWithSlots.Slot_1_Size = 4;
                itemWithSlots.Slot_2_Size = 4;
                itemWithSlots.Slot_3_Size = 4;

                item.OnPropertyChanged();
            }
        }

        private void Btn_set_bonus_cheat_Click(object sender, RoutedEventArgs e) {
            if (string.IsNullOrEmpty(targetFile)) return;

            if (!IsArmor()) return;

            foreach (var item in items) {
                Armor armor = item;

                if (armor.Set_Skill_1_Level > 0) {
                    armor.Set_Skill_1_Level = 5;
                }

                item.OnPropertyChanged();
            }
        }

        private void Btn_skill_level_cheat_Click(object sender, RoutedEventArgs e) {
            if (string.IsNullOrEmpty(targetFile)) return;

            if (!IsGem() && !IsArmor()) return;

            foreach (var item in items) {
                switch (item) {
                    case Gem _: {
                        Gem gem = item;

                        gem.Skill_1_Level = 10;

                        if (gem.Skill_2_Level > 0) {
                            gem.Skill_2_Level = 10;
                        }

                        break;
                    }
                    case Armor _: {
                        Armor armor = item;

                        if (armor.Skill_1_Raw > 0) {
                            armor.Skill_1_Level = 10;
                        }

                        if (armor.Skill_2_Raw > 0) {
                            armor.Skill_2_Level = 10;
                        }

                        if (armor.Skill_3_Raw > 0) {
                            armor.Skill_3_Level = 10;
                        }

                        break;
                    }
                }

                item.OnPropertyChanged();
            }
        }

        private void Btn_zenny_cheat_Click(object sender, RoutedEventArgs e) {
            if (string.IsNullOrEmpty(targetFile)) return;

            if (!IsItem() && !IsArmor() && !IsWeapon()) return;

            foreach (var item in items) {
                switch (item) {
                    case Item _: {
                        Item itm = item;

                        if (itm.Buy_Price > 0) {
                            itm.Buy_Price = 1;
                        }

                        break;
                    }
                    case Armor _: {
                        Armor armor = item;

                        if (armor.Cost > 0) {
                            armor.Cost = 1;
                        }

                        break;
                    }
                    case IWeapon _: {
                        IWeapon weapon = item;

                        if (weapon.Cost > 0) {
                            weapon.Cost = 1;
                        }

                        break;
                    }
                }

                ((dynamic) item).OnPropertyChanged();
            }
        }

        private void Open() {
            var ofdResult = new OpenFileDialog {
                // ReSharper disable StringLiteralTypo
                Filter = "Weapons / Armor / Gems / Items (*.wp_dat/_g, *.am_dat, *.sgpa, *.itm)|*.wp_dat;*.wp_dat_g;*.am_dat;*.sgpa;*.itm",
                Multiselect = false
            };
            ofdResult.ShowDialog();

            targetFile = ofdResult.FileName;
        }

        private void Load() {
            items.Clear();
            Title = Path.GetFileName(targetFile);

            ushort initialOffset;
            ushort itemSize;

            if (IsMelee()) {
                initialOffset = Melee.InitialOffset;
                itemSize = Melee.StructSize;
            } else if (IsRanged()) {
                initialOffset = Ranged.InitialOffset;
                itemSize = Ranged.StructSize;
            } else if (IsArmor()) {
                initialOffset = Armor.InitialOffset;
                itemSize = Armor.StructSize;
            } else if (IsGem()) {
                initialOffset = Gem.InitialOffset;
                itemSize = Gem.StructSize;
            } else if (IsItem()) {
                initialOffset = Item.InitialOffset;
                itemSize = Item.StructSize;
            } else {
                return;
            }

            btn_slot_cheat.Visibility = IsArmor() || IsWeapon() ? Visibility.Visible : Visibility.Collapsed;
            btn_skill_level_cheat.Visibility = IsGem() || IsArmor() ? Visibility.Visible : Visibility.Collapsed;
            btn_set_bonus_cheat.Visibility = IsArmor() ? Visibility.Visible : Visibility.Collapsed;
            btn_zenny_cheat.Visibility = IsItem() || IsArmor() || IsWeapon() ? Visibility.Visible : Visibility.Collapsed;

            using (var dat = new BinaryReader(new FileStream(targetFile, FileMode.Open, FileAccess.Read))) {
                var len = dat.BaseStream.Length;
                int offset = initialOffset;

                do {
                    var buff = new byte[itemSize];
                    dat.BaseStream.Seek(offset, SeekOrigin.Begin);
                    dat.Read(buff, 0, itemSize);

                    dynamic obj = null;

                    if (IsMelee()) {
                        obj = new Melee(buff, offset);
                    } else if (IsRanged()) {
                        if (IsBow()) {
                            obj = new Ranged(buff, offset);
                        } else if (IsBowGun()) {
                            obj = new BowGun(buff, offset);
                        }
                    } else if (IsArmor()) {
                        obj = new Armor(buff, offset);
                    } else if (IsGem()) {
                        obj = new Gem(buff, offset);
                    } else if (IsItem()) {
                        obj = new Item(buff, offset);
                    }

                    if (obj == null) return;

                    items.Add(obj);

                    offset += itemSize;
                } while (offset + itemSize <= len);
            }
        }

        private void Save() {
            using (var dat = new BinaryWriter(new FileStream(targetFile, FileMode.Open, FileAccess.Write))) {
                foreach (var item in items) {
                    IMhwItem obj = item;
                    // First starts at 25, so should be safe.
                    if (obj.Offset == 0 || !obj.Changed) continue;

                    var buff = obj.Bytes;

                    dat.Seek(obj.Offset, SeekOrigin.Begin);
                    dat.Write(buff);
                }
            }
        }

        private bool IsMelee() => Path.GetExtension(targetFile) == ".wp_dat";
        private bool IsRanged() => Path.GetExtension(targetFile) == ".wp_dat_g";
        private bool IsBow() => Path.GetFileName(targetFile).StartsWith("bow");
        private bool IsBowGun() => Path.GetFileName(targetFile).StartsWith("lbg") || Path.GetFileName(targetFile).StartsWith("hbg");
        private bool IsWeapon() => IsMelee() || IsRanged();
        private bool IsArmor() => Path.GetExtension(targetFile) == ".am_dat";
        private bool IsGem() => Path.GetExtension(targetFile) == ".sgpa";
        private bool IsItem() => Path.GetExtension(targetFile) == ".itm";
    }
